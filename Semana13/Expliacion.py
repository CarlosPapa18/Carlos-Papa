## **Objetivo**

"Desarrollar una aplicación de interfaz gráfica de usuario (GUI) que permita a los usuarios interactuar con datos de manera visual, agregando, eliminando y limpiando información de una lista."

##---

## **Descripción de la Aplicación**

"Esta aplicación permite:"

"- Ingresar información en un campo de texto."
"- Agregar la información a una lista."
"- Limpiar el campo de texto o eliminar un elemento seleccionado."
"- Agregar elementos presionando la tecla `Enter`."
"- Eliminar elementos con doble clic sobre ellos."
"- Evitar elementos duplicados en la lista."

##---

## **Componentes de la Interfaz**

"- **Etiqueta (Label):** Indica al usuario dónde ingresar información."
"- **Campo de texto (Entry):** Para escribir la información a agregar."
"- **Botón Agregar:** Añade el texto a la lista."
"- **Botón Limpiar:** Borra el texto del campo o elimina el elemento seleccionado."
"- **Lista (Listbox):** Muestra los elementos agregados."

